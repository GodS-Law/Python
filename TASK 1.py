#This is for taking input in float values. f_no is first number and s_no is the second number.
f_no = float(input("Enter the first number: "))
s_no = float(input("Enter the second number: "))

#For calculating.
add = f_no + s_no
sub = f_no - s_no
multi = f_no * s_no
div = f_no / s_no

#For showing the values.
print(f"Addition: {add}\nSubtraction: {sub}\nMultiplication: {multi}\nDivision: {div}")

